#To input two numbers and perform all arithmetic operations on them

a = int(input("Enter first number:"))
b = int(input("Enter second number:"))

#Operations
print("Sum:", a + b)

print("Difference:", a - b)

print("Product:", a * b)

print("Quotient:", a / b)

print("Remainder:", a % b)

print("Integer Quotient:", a // b)

print("Exponent:", a ** b)
